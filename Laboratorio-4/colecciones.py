print("============================= Colecciones en Python =============================\n")

#Colecciones en Python.
myFruits = ["Manzana", "Sandia", "Banano"]

print(myFruits)

print (type(myFruits))

#Imprimir por indice.
print(myFruits[0])

print(myFruits[1])

print(myFruits[2])

#Cambiar valores de la lista.
myFruits[2] = "Naranja"

print(myFruits)

#Colección definitiva.
print("\n\n")

myFinalAnswerTuple = ("Manzana", "Banano", "Piña")

