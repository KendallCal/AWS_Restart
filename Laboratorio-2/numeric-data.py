#Tipos de valores n√∫mericos en Python.
print("Python tiene tres tipos de valores numericos: int, float, y complex\n")

#Tipo de valor Entero.
myValue = 1

print(" - - - - - - - Tipo Entero (int) - - - - - - - ")

print(myValue)

print(type(myValue))

print(str(myValue) + " es de tipo: " + str(type(myValue)) + "\n\n")


#Tipo de valor Float.
myValue = 3.14

print(" - - - - - - - Tipo Flotante (float) - - - - - - - ")

print(myValue)

print(type(myValue))

print(str(myValue) + " es de tipo " + str(type(myValue)) + "\n\n")


#Tipo de valor Complex.
myValue = 5j

print(" - - - - - - - Tipo Complejo (complex) - - - - - - - ")

print(myValue)

print(type(myValue))

print(str(myValue) + " es de tipo " + str(type(myValue)) + "\n\n")

#Tipo de valor Bool.
print(" - - - - - - - Tipo Booleano (bool) - - - - - - - ")

#Tipo Verdadero.
